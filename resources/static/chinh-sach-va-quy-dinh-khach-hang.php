4. QUY ĐỊNH KHI ĐẶT DỊCH VỤ
Khi đặt dịch vụ trên nền tảng Sự Kiện Tốt, khách hàng cần cung cấp đầy đủ các thông tin liên quan đến sự kiện để nền tảng có cơ sở đề xuất dịch vụ, báo giá và điều phối phù hợp.
Thông tin khách hàng cần cung cấp gồm:
	•	Tên người đặt dịch vụ hoặc tên tài khoản đặt dịch vụ.
	•	Thời gian tổ chức sự kiện.
	•	Địa điểm tổ chức sự kiện.
	•	Loại sự kiện như sinh nhật, khai trương, thôi nôi, tiệc gia đình, lễ hội, chương trình công ty, tổng kết, tất niên hoặc các sự kiện khác.
	•	Dịch vụ cần đặt như MC, chú hề, ảo thuật, mascot, múa lân, trang trí, biểu diễn, hỗ trợ, hậu cần hoặc các hạng mục liên quan.
	•	Quy mô chương trình hoặc số lượng khách dự kiến nếu có.
	•	Yêu cầu đặc biệt của chương trình nếu có.
Thông tin càng đầy đủ, nền tảng càng dễ đề xuất dịch vụ phù hợp, báo giá chính xác và hỗ trợ khách hàng tốt hơn.
Thông tin liên hệ của khách hàng sẽ được sử dụng theo dữ liệu tài khoản đã đăng ký trên nền tảng. Khách hàng không cần nhập lại số điện thoại trong từng yêu cầu đặt dịch vụ, trừ khi có nhu cầu cập nhật hoặc thay đổi thông tin liên hệ.

