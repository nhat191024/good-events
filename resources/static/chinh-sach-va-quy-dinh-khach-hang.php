Không sử dụng nền tảng để lừa đảo, gây rối, phá hoại, truyền tải nội dung sai lệch, xúc phạm, quấy rối hoặc thực hiện các hành vi vi phạm pháp luật.
Không tự ý sử dụng thông tin của nhân sự dịch vụ, khách hàng khác hoặc dữ liệu thuộc nền tảng cho mục đích ngoài phạm vi đơn dịch vụ.
Không chuyển giao, cho mượn, bán hoặc chia sẻ tài khoản của mình cho người khác sử dụng khi chưa được Sự Kiện Tốt đồng ý.
Tự chịu trách nhiệm bảo mật thông tin đăng nhập, mật khẩu và các hoạt động phát sinh từ tài khoản của mình.
