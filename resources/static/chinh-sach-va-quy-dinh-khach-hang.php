Thời điểm tổ chức như ngày thường, cuối tuần, ngày lễ, Tết hoặc khung giờ đặc biệt.
Báo giá chỉ có hiệu lực trong thời gian được thông báo. Trường hợp khách hàng thay đổi thời gian, địa điểm, nội dung chương trình, số lượng nhân sự hoặc phát sinh thêm hạng mục, báo giá có thể được điều chỉnh lại cho phù hợp với thực tế.
Các chương trình khuyến mại, ưu đãi hoặc giảm giá nếu có chỉ áp dụng theo đúng điều kiện, thời gian và đối tượng được Sự Kiện Tốt công bố.

6. XÁC NHẬN ĐƠN DỊCH VỤ
Đơn dịch vụ chỉ được xem là xác nhận khi khách hàng hoàn tất các bước theo quy trình của nền tảng, bao gồm một hoặc nhiều điều kiện sau:
Khách hàng đồng ý với báo giá.
Khách hàng xác nhận đầy đủ thông tin dịch vụ.
Khách hàng hoàn tất đặt cọc hoặc thanh toán theo quy định.
Nền tảng hoặc nhân sự dịch vụ xác nhận lịch thực hiện.
Khách hàng nhận được thông báo xác nhận đơn dịch vụ từ hệ thống hoặc bộ phận phụ trách.
Khách hàng nên xác nhận đơn sớm để giữ lịch, đặc biệt vào các thời điểm cao điểm như cuối tuần, lễ, Tết, mùa khai trương, mùa tổng kết, mùa tiệc thiếu nhi hoặc các giai đoạn nhu cầu sự kiện tăng cao.
Sự Kiện Tốt không chịu trách nhiệm giữ lịch đối với các yêu cầu chưa được xác nhận hoặc chưa hoàn tất đặt cọc theo quy định.

7. CHÍNH SÁCH ĐẶT CỌC VÀ THANH TOÁN
Để đảm bảo quyền lợi cho khách hàng, nền tảng và nhân sự dịch vụ, khách hàng cần thực hiện đặt cọc hoặc thanh toán theo quy định của từng đơn dịch vụ.
Thông thường, khách hàng đặt cọc để giữ lịch, sau đó thanh toán phần còn lại sau khi dịch vụ hoàn thành, trừ khi hai bên có thỏa thuận khác.
Đối với các đơn hàng có giá trị lớn, cần chuẩn bị đạo cụ, trang trí, vật tư, thiết bị, nhân sự số lượng nhiều hoặc đặt vào khung giờ cao điểm, Sự Kiện Tốt có thể yêu cầu mức đặt cọc cao hơn.
Sau khi khách hàng hoàn tất đặt cọc, nền tảng sẽ tiến hành giữ lịch, điều phối nhân sự, chuẩn bị đạo cụ, vật tư và các hạng mục liên quan.
Khách hàng có thể thanh toán bằng các phương thức được Sự Kiện Tốt hỗ trợ tại từng thời điểm, bao gồm chuyển khoản, thanh toán qua nền tảng hoặc các hình thức thanh toán hợp lệ khác.
Mọi khoản thanh toán cần được thực hiện đúng thời hạn. Trường hợp khách hàng chậm thanh toán, Sự Kiện Tốt có quyền tạm dừng xử lý đơn dịch vụ hoặc điều chỉnh lịch thực hiện nếu cần thiết.

8. CHÍNH SÁCH THAY ĐỔI THÔNG TIN ĐƠN DỊCH VỤ
Khách hàng cần thông báo sớm nếu có thay đổi về:
Thời gian tổ chức.
Địa điểm tổ chức.
Hạng mục dịch vụ.
