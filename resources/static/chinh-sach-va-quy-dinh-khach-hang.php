Quy mô sự kiện.
Yêu cầu biểu diễn, trang trí hoặc hỗ trợ.
Trường hợp thay đổi được thông báo sớm, Sự Kiện Tốt sẽ hỗ trợ điều chỉnh trong khả năng cho phép.
Trường hợp thay đổi quá sát giờ diễn ra sự kiện, nền tảng sẽ cố gắng hỗ trợ nhưng không cam kết đáp ứng toàn bộ yêu cầu mới. Nếu việc thay đổi làm phát sinh thêm chi phí di chuyển, thời gian chờ, nhân sự, đạo cụ, vật tư hoặc thiết bị, khách hàng vui lòng thanh toán khoản chi phí phát sinh tương ứng.

9. CHÍNH SÁCH HỦY ĐƠN DỊCH VỤ
Khách hàng có thể hủy đơn dịch vụ, tuy nhiên cần thông báo sớm để Sự Kiện Tốt và nhân sự dịch vụ kịp thời điều chỉnh lịch làm việc.
Chính sách hủy đơn được áp dụng theo tình trạng thực tế của từng đơn dịch vụ:
Trường hợp khách hàng hủy trước khi đơn được xác nhận, nền tảng sẽ ghi nhận và kết thúc yêu cầu.
Trường hợp khách hàng hủy sau khi đã xác nhận đơn hoặc đã đặt cọc, tiền cọc có thể không được hoàn lại nếu nền tảng hoặc nhân sự dịch vụ đã tiến hành giữ lịch, chuẩn bị nhân sự, đạo cụ, vật tư hoặc từ chối các đơn hàng khác trong cùng khung giờ.
Trường hợp khách hàng hủy sát giờ diễn ra sự kiện, khách hàng có thể mất cọc hoặc chịu một phần chi phí đã phát sinh.
Trường hợp khách hàng hủy khi nhân sự đã di chuyển hoặc đã đến địa điểm tổ chức, khách hàng cần thanh toán chi phí phù hợp theo tình trạng thực tế.
Trường hợp hủy do thời tiết, sự cố khách quan hoặc lý do bất khả kháng, Sự Kiện Tốt sẽ hỗ trợ khách hàng trao đổi phương án dời lịch, thay đổi dịch vụ hoặc xử lý theo tình hình thực tế.
Tiền cọc được sử dụng cho mục đích giữ lịch, chuẩn bị nhân sự, vật tư, đạo cụ và chi phí vận hành ban đầu, nên sẽ không hoàn lại trong các trường hợp khách hàng hủy sát ngày, sát giờ hoặc khi đơn dịch vụ đã phát sinh chi phí chuẩn bị.

10. CHÍNH SÁCH THỜI GIAN LÀM VIỆC
Khách hàng cần bố trí đúng thời gian đã đặt để nhân sự dịch vụ thực hiện công việc hiệu quả.
Nếu chương trình bắt đầu trễ do phía khách hàng, thời gian dịch vụ vẫn được tính theo khung giờ đã thỏa thuận ban đầu.
Trường hợp nhân sự đã đến đúng giờ nhưng khách hàng chưa sẵn sàng bàn giao vị trí, chưa chuẩn bị không gian hoặc chương trình bị trì hoãn, thời gian chờ vẫn được tính vào thời lượng dịch vụ.
Trường hợp khách hàng muốn kéo dài thêm thời gian biểu diễn, hoạt náo, hỗ trợ, trang trí hoặc làm việc, khách hàng cần trao đổi trước với nền tảng hoặc nhân sự dịch vụ. Phần thời gian phát sinh sẽ được tính thêm chi phí nếu nhân sự có thể tiếp tục hỗ trợ.
Sự Kiện Tốt không cam kết nhân sự có thể gia hạn thời gian trong mọi trường hợp, đặc biệt khi nhân sự đã có lịch làm việc tiếp theo.

11. QUY ĐỊNH TẠI ĐỊA ĐIỂM TỔ CHỨC
Khách hàng cần hỗ trợ các điều kiện cơ bản để dịch vụ diễn ra thuận lợi, bao gồm:
Cung cấp vị trí biểu diễn, thi công hoặc làm việc phù hợp.
Đảm bảo không gian an toàn cho nhân sự, khách mời và trẻ em.
Hỗ trợ nguồn điện nếu dịch vụ cần sử dụng âm thanh, ánh sáng, thiết bị hoặc công cụ hỗ trợ.
Thông báo trước các quy định riêng của địa điểm tổ chức như giờ ra vào, khu vực gửi xe, thang máy, bốc xếp, giấy phép, phí mặt bằng hoặc quy định bảo vệ.
Hỗ trợ nhân sự ra vào khu vực sự kiện nếu địa điểm có quy trình kiểm soát.
Với các dịch vụ biểu diễn, hoạt náo, múa lân, mascot, ảo thuật, chú hề hoặc trang trí, khách hàng nên chuẩn bị khu vực đủ rộng để đảm bảo chất lượng chương trình và an toàn trong quá trình thực hiện.
