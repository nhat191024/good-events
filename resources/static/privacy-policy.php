Trách nhiệm của công ty đối với thông tin cá nhân của người dùng (nhân sự và khách hàng)

PHẦN A – Các loại dữ liệu cá nhân chúng tôi thu thập và xử lý

Chúng tôi có thể thu thập và xử lý các loại dữ liệu sau:

1. Thông tin nhận dạng cá nhân

Tên, số CMND/CCCD, số hộ chiếu, quốc tịch, địa chỉ cư trú, số điện thoại, email, giới tính, ngày sinh, dân tộc, nghề nghiệp, thông tin thanh toán – ngân hàng, tình trạng hôn nhân và các thông tin Người dùng cung cấp trong biểu mẫu đăng ký.

2. Thông tin về mối quan hệ kinh doanh

Nhu cầu dịch vụ, phản hồi, khảo sát, lịch sử giao dịch và trao đổi phát sinh.

3. Dữ liệu điện tử

IP địa chỉ, cookie, cập nhật quyền truy cập, ID thiết bị, vị trí, trình duyệt thông tin, MAC địa chỉ và phát hiện dữ liệu khi người dùng truy cập trang web hoặc ứng dụng.

4.Sensitive data

Bao gồm: dữ liệu sinh trắc học, tình trạng sức khỏe, tôn giáo, thông tin lý lịch tư pháp,… Chỉ được xử lý khi có đồng ý rõ ràng và tuân thủ quy định pháp luật.

5. Thu thập dữ liệu từ các nguồn khác

Từ các dịch vụ, chương trình giới thiệu, nguồn công khai hoặc cá nhân khác khi họ cung cấp thông tin của bạn.

6. Khi người dùng cung cấp dữ liệu của người khác

• Bạn đã cam kết có ý kiến • hợp lý từ cá nhân đó.

7. Dữ liệu của trẻ thành niên

Chúng tôi không thu thập dữ liệu của những người dưới 18 tuổi nếu không có đồng ý của cha mẹ hoặc người giám hộ.

PHẦN B – Cách thức chúng tôi thu thập dữ liệu

Chúng tôi thu thập dữ liệu cá nhân thông qua:

• Biểu mẫu đăng ký và hồ sơ Người dùng cung cấp.

• Tương tác qua điện thoại, email, mạng xã hội hoặc trao đổi trực tiếp.

• Hoạt động truy cập và sử dụng website/ứng dụng.

• Tham gia khảo sát, cuộc thi, sự kiện, chương trình ưu đãi.

• Bên thứ ba khi được Người dùng cho phép chia sẻ dữ liệu.

PHẦN C – Mục đích xử lý dữ liệu cá nhân

1. Đối với Khách hàng

• Thực hiện các nghĩa vụ theo hợp đồng.

• Cung cấp sản phẩm/dịch vụ theo yêu cầu.

• Xử lý đăng ký, giao dịch và thanh toán.

• Hỗ trợ kỹ thuật, chăm sóc khách hàng và duy trì liên lạc.

• Cải thiện, nâng cao và cá nhân hóa trải nghiệm dịch vụ.

• Thực hiện báo cáo và tuân thủ yêu cầu pháp luật.


2. Đối với Cộng tác viên/Đối tác dịch vụ

• Xác minh danh tính, hồ sơ và năng lực.

• Ký kết, thực hiện và quản lý hợp đồng hợp tác.

• Quản lý hiệu suất và chất lượng dịch vụ.

• Xử lý thanh toán và các nghĩa vụ liên quan.

• Hỗ trợ và duy trì liên lạc.

• Thực hiện yêu cầu từ cơ quan có thẩm quyền.

3. Mục đích tiếp thị – quảng bá – truyền thông

Khi Người dùng tham gia tài khoản, sự kiện, chương trình khuyến mãi hoặc cộng đồng của Sự Kiện Tốt, Người dùng đồng ý cấp cho chúng tôi quyền sử dụng hình ảnh, tên, giọng nói và các nội dung đánh giá dịch vụ cho mục đích truyền thông – quảng cáo.

Việc sử dụng hình ảnh luôn đảm bảo tôn trọng danh dự, nhân phẩm và không gây ảnh hưởng tiêu cực. Người dùng có thể rút lại sự đồng ý bất kỳ lúc nào bằng cách gửi yêu cầu chính thức.

PHẦN D – Chia sẻ dữ liệu cá nhân

Dữ liệu có thể được chia sẻ trong các trường hợp:

• Có sự đồng ý của Người dùng.

